"""Planning 프레임워크 — 계획 수립 회의 요약."""

from app.prompts.common import build_prompt

NAME = "planning"
DESCRIPTION = (
    "Planning: 계획 수립 회의. 목표, 일정, 리소스, 단계, 우선순위 같은 "
    "앞으로 무엇을 어떻게 할지 설계하는 내용에 적합."
)

OUTPUT_FORMAT = """\
[출력 형식]
1. 논의 배경
- 이 기획 회의가 열리게 된 맥락과 목적 (transcript에서 명시된 것만).
- 다음이 transcript에 드러나면 반드시 포함한다: 무엇을 정하려는 회의인지(목표), 산출물이 누구에게/어디에 쓰이는지(예: 보고 대상·문서·제출처), 마감/기한, 다루는 대상의 범위(예: 후보자 몇 명).
- 막연히 "~에 대한 논의"로 끝내지 말고, 회의가 만들어내려는 결과물이 무엇인지까지 적는다.

2. 주요 아이디어 / 제안
- 논의된 "대상"(검토 중인 후보자·안건·프로젝트 등)별로 묶어, 각 대상에 대해 어떤 제안·의견이 오갔는지 종합한다.
- 묶음 라벨은 "논의 대상"의 실제 이름이다. (예: "[이지용] ...", "[홍은서] ...") 발화자(참석자N)로 묶지 말 것.
- 각 대상마다 제안된 역할·과제·배치를 구체적으로 적되, 확정이 아니라 제안·검토 단계임을 서법에서 유지한다.
  (예: "튜링 매니저 역할이 적합하다는 의견", "AIND 기획 활용이 제안됨" — "~로 결정"이 아님)
- 같은 대상에 대한 여러 발언은 하나로 묶어 종합한다. 발언을 한 줄씩 나열하지 말 것.

3. 선택된 방향 (Decision)
- 회의 중 화자가 명시적으로 확정·합의한 방향을 기재한다.
- 최종 배치·역할 같은 본안뿐 아니라, 진행 절차에 대한 합의(예: "전원 컨택하기로", "일단 ~로 넣어두기로", "~까지 보고하기로")도 명시적으로 합의됐다면 여기에 포함한다.
- 제안·희망·"근거를 더 찾아보자" 수준에 머문 항목은 절대 여기에 넣지 말 것. 확정된 것이 없으면 "- 해당 내용 없음".

4. 보류 / 미결
- 논의되었으나 채택되지 않았거나 결론에 이르지 못한 안.
- 다음도 빠짐없이 포착한다: "추후 검토"·"근거 마련 필요" 발언, 특정 배치를 넣고 싶지만 근거가 빈약해 합의에 못 이른 경우, 한 번 제안됐다가 철회·반대된 안(예: 어떤 역할 표현을 쓰지 않기로 기운 경우).

5. 다음 액션
- [담당자] 후속 작업 내용
- 담당자 미언급: [미정]
- 기한·마감이 언급됐으면 액션에 함께 적는다.

6. 기타 논의
- 위 1~5번 섹션에 들어가지 않지만 회의에서 다뤄진 내용 (없으면 "- 해당 내용 없음")

[이 프레임워크 추가 규칙]
- 채택된 안과 보류된 안을 명확히 구분한다. 분류가 모호한 항목은 강제로 분류하지 말고 "주요 아이디어 / 제안"에만 둔다.
- 결정 근거는 transcript에 언급된 경우에만 함께 기재한다. 언급되지 않으면 추가하지 말 것.\
"""

SYSTEM_PROMPT = build_prompt(OUTPUT_FORMAT)
